        [Test]
        public void CalcularGenero()
        {
            var genero = new Genero();
            Assert.AreEqual(false, genero.NomeEhMasculino("MARCELA"));
            Assert.AreEqual(false, genero.NomeEhMasculino("vivian sella"));
            Assert.AreEqual(false, genero.NomeEhMasculino("vivian joao"));
            Assert.AreEqual(false, genero.NomeEhMasculino("judite"));

            Assert.AreEqual(true, genero.NomeEhMasculino("joao"));
            Assert.AreEqual(true, genero.NomeEhMasculino("joao da silva"));
            Assert.AreEqual(true, genero.NomeEhMasculino("joao das dores"));
            Assert.AreEqual(true, genero.NomeEhMasculino("caim abrao"));

            Assert.AreEqual(true, genero.NomeEhMasculino("marcio"));

            Assert.AreEqual(true, genero.NomeEhMasculino("Jeff"));
        }

